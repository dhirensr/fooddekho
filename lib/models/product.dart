class Product {
  final String name;
  final String servingSizeQuantity;
  final String energyKcal;
  final String fat;
  final String saturatedFat;
  final String transFat;
  final String monounsaturatedFat;
  final String polyunsaturateFat;
  final String carbohydrates;
  final String sugar;
  final String fibre;
  final String protein;
  final String sodium;
  final String cholestrol;
  final String potassium;

  Product(this.name, this.servingSizeQuantity, this.energyKcal, this.fat,
  this.saturatedFat, this.transFat, this.monounsaturatedFat, this.polyunsaturateFat, 
  this.carbohydrates, this.sugar, this.fibre, this.protein, this.sodium,
  this.cholestrol, this.potassium);
}